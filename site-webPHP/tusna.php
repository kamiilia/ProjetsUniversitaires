<h1>Présentation Tusna Béjaia</h1>
<div style="height:600px;overflow:auto;">
 <img src="images/formations/gestion.jpg" width="233" height="216" alt="Ecole tusna" style="box-shadow:10px 10px 30px #CCCCCC;float:left; margin-right:20px;margin-bottom:15px">
 <p><strong>TUSNA</strong>,un mot évoquant le savoir est celui qu&rsquo;a  choisi un groupe de jeunes diplômés comme appellation pour leur projet d&rsquo;école  de formation professionnelle.<br>
   <br>
   <br>
   Le début de cette  aventure voit le jour en 1995 à Amizour sous le n°&nbsp; d&rsquo;agrément 2482/95  suivant le décret exécutif n° 91-141 du 11 mai 1991 fixant les conditions de  création, d&rsquo;ouverture et de contrôle des établissements privés de formation  professionnelle. Elle est le fruit des efforts et de la contribution de quatre  jeunes diplômés, décidés à mettre leur savoir-faire et l&rsquo;apport de leur  spécialité en électronique, informatique et Sciences Commerciales au service  des jeunes&nbsp;; des spécialités qui font l&rsquo;objet d&rsquo;une demande incessante de  carrière professionnelle.<br>
  <br>
  <br>
  <br>
   TUSNA Bejaia est aussi  une équipe qualifiée et un savoir-faire, résultat de presque deux décennies au  service de la formation. Elle collabore aujourd&rsquo;hui à la création de plusieurs  emplois directs et indirects, en effet elle ne cesse de contribuer au déploiement  d&rsquo;un potentiel humain bien avancé depuis sa création.<br>
  <br>
  <br>
   TUSNA Bejaia, déjà  l&rsquo;une des leaders des écoles agréées de formation au niveau de la wilaya, est  aussi un investissement de bienfaisance, qui a toujours répondu aux plus&nbsp;  démunis.<br>
  <br>
  <br>
   En effet, depuis des  années, elle déploie tous ses efforts pour venir en aide aux autres, ou sur  l&rsquo;ensemble des diplômés formés&nbsp;; 25% font office d&rsquo;une contribution  purement caritative, chose qui prouve sa réelle politique de vocation de  réussite sociale&nbsp;: une aide pour une complète insertion professionnelle et  de réalisation des ambitions de carrière des jeunes formés au sein de ses  établissements..<br>
  <br>
  <br>
   TUSNA Bejaia se voit  projeter dans l&rsquo;avenir d&rsquo;autres perspectives, décidée à aller plus loin dans  ses prestations de service.Consciente de a croissante demande de  formation&nbsp;; elle est déterminée à continuer d&rsquo;instaurer une politique de  savoir, traçant de plus grands objectifs.</p>
